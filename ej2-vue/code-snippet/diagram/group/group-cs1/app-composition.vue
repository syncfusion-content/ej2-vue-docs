<template>
    <div id="app">
        <ejs-diagram ref="diagramRef" id="diagram" width="100%" height="600px" :getNodeDefaults="getNodeDefaults"
            :nodes="nodes" />
    </div>
</template>

<script setup>
import { ref, onMounted } from 'vue';
import { DiagramComponent as EjsDiagram } from '@syncfusion/ej2-vue-diagrams';

const nodes = [
    {
        id: "rectangle1",
        offsetX: 100,
        offsetY: 100,
        width: 100,
        height: 100,
        style: {
            strokeColor: "#6BA5D7",
            fill: "#6BA5D7",
        },
        annotations: [
            {
                content: "rectangle1",
            },
        ],
    },
    {
        id: "rectangle2",
        offsetX: 200,
        offsetY: 200,
        width: 100,
        height: 100,
        style: {
            strokeColor: "#6BA5D7",
            fill: "#6BA5D7",
        },
        annotations: [
            {
                content: "rectangle2",
            },
        ],
    },
    {
        id: "group",
        children: ["rectangle1", "rectangle2"],
    },
];

function getNodeDefaults(node) {
    node.height = 100;
    node.width = 100;
    node.style.strokeColor = "White";
    return node;
}

const diagramRef = ref(null);

onMounted(() => {
    // Retrieve the diagram instance
    const diagramInstance = diagramRef.value.ej2Instances;
    diagramInstance.select([diagramInstance.getObject("group")]);
});
</script>

