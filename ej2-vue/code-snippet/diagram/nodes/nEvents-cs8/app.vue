<template>
  <div id="app">
    <ejs-diagram
      id="diagram"
      ref="diagram"
      :width="width"
      :height="height"
      :nodes="nodes"
      :mouseEnter="mouseEnter"
      :mouseOver="mouseOver"
      :mouseLeave="mouseLeave"
    ></ejs-diagram>
  </div>
</template>
<script>
import { DiagramComponent } from '@syncfusion/ej2-vue-diagrams';

let nodes = [
  {
    // Position of the node
    offsetX: 250,
    offsetY: 250,
    // Size of the node
    width: 100,
    height: 100,
    style: {
      fill: '#6BA5D7',
      strokeColor: 'white',
    },
  },
];

export default {
  name: 'App',
  components: {
    'ejs-diagram': DiagramComponent,
  },
  data() {
    return {
      width: '100%',
      height: '700px',
      nodes: nodes,
      mouseEnter: (args) => {
        args.actualObject.style.fill = 'red';
        this.$refs.diagram.ej2Instances.dataBind();
      },
      mouseLeave: (args) => {
        args.element.style.fill = '#6BA5D7';
        this.$refs.diagram.ej2Instances.dataBind();
      },
      mouseOver: (args) => {
        //Cutomize
      },
    };
  },
};
</script>
